my_string = 'soy un curso facilito'
print(my_string[1]) # Los strings son como arrays o listas y por tanto cada caracter ocupa su respectiva posicion iniciando en 0

print(my_string[-1]) # Regresa el ultimo caracter
print(my_string[-2]) # Regresa el penultimo caracter 

print(my_string[0:5]) # Regresa un rango de caracteres, pero no incluye el 5to
print(my_string[5])

print(my_string[-2:-1]) # Regresa desde inicial a final, es decir la "t"
print(my_string[0:10:2]) # Regresa los caracteres de a 2 posiciones, iniciando en la 0 hasta la 10

print(my_string[::-1]) # Regresa la cadena de caracteres de atras hacia adelante