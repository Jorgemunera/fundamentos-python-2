def suma(num1, num2):
    """regresa el resultado de sumar 2 numeros"""
    return num1 + num2

def resta(num1, num2):
    """ regresa la resta de 2 numeros"""
    return num1 - num2

def producto(num1, num2):
    """regresa el producto de 2 numeros"""
    return num1 * num2

def division(num1, num2):
    """regresa la division de 2 numeros"""
    return num1 / num2
